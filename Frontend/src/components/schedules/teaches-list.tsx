'use client';

import React, { useEffect, useState } from 'react';
import {
  Box,
  Button,
  Dialog,
  DialogTitle,
  DialogContent,
  DialogActions,
  TextField,
  Select,
  MenuItem,
  FormControl,
  InputLabel,
  Table,
  TableBody,
  TableCell,
  TableContainer,
  TableHead,
  TableRow,
  Paper,
  Chip,
  Alert,
  CircularProgress,
} from '@mui/material';
import { Teach } from '@/types/schedule';
import { useTeaches } from '@/hooks/useTeaches';
import { useTeachers } from '@/hooks/use-teachers';
import { useSubjects } from '@/hooks/use-subjects';

export function TeachesList() {
  const { teaches, loading, error, fetchAll } = useTeaches();
  const { teachers } = useTeachers();
  const { subjects } = useSubjects();
  const [open, setOpen] = useState(false);
  const [editingId, setEditingId] = useState<string | null>(null);
  const [selectedTeacherId, setSelectedTeacherId] = useState<string>('');
  const [submitting, setSubmitting] = useState(false);
  const [submitError, setSubmitError] = useState<string | null>(null);
  const [allSubjects, setAllSubjects] = useState<typeof subjects>([]);
  const [formData, setFormData] = useState<Partial<Teach>>({
    teacherId: '',
    subjectId: '',
    className: '',
    classType: 'fixed',
    dayOfWeek: 'Th·ª© 2',
    startTime: '08:00',
    endTime: '10:00',
    notes: '',
  });

  // Fetch all subjects on mount
  useEffect(() => {
    const fetchAllSubjects = async () => {
      try {
        const apiUrl = process.env.NEXT_PUBLIC_API_URL || 'http://localhost:5000';
        const url = apiUrl.endsWith('/api') ? `${apiUrl}/subjects?limit=999` : `${apiUrl}/api/subjects?limit=999`;
        console.log('üîç Fetching all subjects from:', url);
        const response = await fetch(url);
        if (response.ok) {
          const data = await response.json();
          console.log('‚úÖ Fetched subjects:', {
            count: data.data?.length,
            firstSubject: data.data?.[0],
            lastSubject: data.data?.[data.data.length - 1],
          });
          if (data.success && Array.isArray(data.data)) {
            setAllSubjects(data.data);
          }
        } else {
          console.error('‚ùå Failed to fetch subjects:', response.status);
        }
      } catch (err) {
        console.error('‚ùå Failed to fetch all subjects:', err);
      }
    };
    fetchAllSubjects();
  }, []);

  // Filter teaches by selected teacher
  const filteredTeaches = selectedTeacherId 
    ? teaches.filter(t => String(t.teacherId) === selectedTeacherId)
    : teaches;

  useEffect(() => {
    fetchAll();
  }, [fetchAll]);

  const handleOpen = () => {
    setFormData({
      teacherId: '',
      subjectId: '',
      className: '',
      classType: 'fixed',
      dayOfWeek: 'Th·ª© 2',
      startTime: '08:00',
      endTime: '10:00',
      notes: '',
    });
    setEditingId(null);
    setOpen(true);
  };

  const handleClose = () => {
    setOpen(false);
    setEditingId(null);
    setSubmitError(null);
  };

  const handleSubmit = async () => {
    try {
      setSubmitting(true);
      setSubmitError(null);

      // Validation
      if (!formData.teacherId || !formData.subjectId || !formData.className || !formData.dayOfWeek || !formData.startTime || !formData.endTime) {
        setSubmitError('Vui l√≤ng ƒëi·ªÅn t·∫•t c·∫£ c√°c tr∆∞·ªùng b·∫Øt bu·ªôc');
        return;
      }

      const apiUrl = process.env.NEXT_PUBLIC_API_URL || 'http://localhost:5000';
      const endpoint = apiUrl.endsWith('/api') ? `${apiUrl}/teaches` : `${apiUrl}/api/teaches`;

      console.log('üì§ Creating teach with data:', formData);
      console.log('üì§ Endpoint:', endpoint);

      // Create
      const response = await fetch(endpoint, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(formData),
      });

      console.log('üì° Response status:', response.status);
      
      const responseData = await response.json();
      console.log('üì° Response data:', responseData);

      if (!response.ok) {
        throw new Error(responseData.message || responseData.error || 'L·ªói t·∫°o ph√¢n c√¥ng d·∫°y');
      }

      console.log('‚úÖ Teach created successfully');
      await fetchAll();
      handleClose();
    } catch (err) {
      const errorMsg = err instanceof Error ? err.message : 'C√≥ l·ªói x·∫£y ra';
      console.error('‚ùå Error creating teach:', errorMsg);
      setSubmitError(errorMsg);
    } finally {
      setSubmitting(false);
    }
  };

  const handleDelete = async (id: string) => {
    if (!confirm('B·∫°n c√≥ ch·∫Øc ch·∫Øn mu·ªën x√≥a ph√¢n c√¥ng d·∫°y n√†y?')) {
      return;
    }

    try {
      const apiUrl = process.env.NEXT_PUBLIC_API_URL || 'http://localhost:5000';
      const endpoint = apiUrl.endsWith('/api') ? `${apiUrl}/teaches/${id}` : `${apiUrl}/api/teaches/${id}`;

      console.log('üóëÔ∏è Deleting teach with id:', id);
      console.log('üóëÔ∏è Endpoint:', endpoint);

      const response = await fetch(endpoint, {
        method: 'DELETE',
      });

      console.log('üì° Delete response status:', response.status);
      
      const responseData = await response.json();
      console.log('üì° Delete response data:', responseData);

      if (!response.ok) {
        throw new Error(responseData.message || responseData.error || 'L·ªói x√≥a ph√¢n c√¥ng d·∫°y');
      }

      console.log('‚úÖ Teach deleted successfully');
      await fetchAll();
    } catch (err) {
      const errorMsg = err instanceof Error ? err.message : 'C√≥ l·ªói x·∫£y ra';
      console.error('‚ùå Error deleting teach:', errorMsg);
      alert('L·ªói x√≥a: ' + errorMsg);
    }
  };

  const classTypeColors: Record<string, 'default' | 'primary' | 'secondary' | 'error' | 'info' | 'success' | 'warning'> = {
    fixed: 'success',
    session: 'info',
  };

  const classTypeLabels: Record<string, string> = {
    fixed: 'L·ªõp c·ªë ƒë·ªãnh',
    session: 'L·ªõp ngo√†i kh√≥a',
  };

  const daysOfWeek = ['Th·ª© 2', 'Th·ª© 3', 'Th·ª© 4', 'Th·ª© 5', 'Th·ª© 6', 'Th·ª© 7', 'Ch·ªß Nh·∫≠t'];

  if (loading) {
    return (
      <Box sx={{ display: 'flex', justifyContent: 'center', py: 4 }}>
        <CircularProgress />
      </Box>
    );
  }

  if (error) {
    return <Alert severity="error">{error}</Alert>;
  }

  return (
    <Box>
      <Box sx={{ mb: 3, display: 'flex', gap: 2, alignItems: 'flex-end' }}>
        <FormControl sx={{ minWidth: 250 }}>
          <InputLabel>L·ªçc theo gi√°o vi√™n</InputLabel>
          <Select
            value={selectedTeacherId}
            onChange={(e) => setSelectedTeacherId(e.target.value)}
            label="L·ªçc theo gi√°o vi√™n"
          >
            <MenuItem value="">T·∫•t c·∫£ gi√°o vi√™n</MenuItem>
            {teachers.map((teacher) => (
              <MenuItem key={teacher.id} value={String(teacher.id)}>
                {teacher.name}
              </MenuItem>
            ))}
          </Select>
        </FormControl>
        <Button
          variant="contained"
          onClick={() => handleOpen()}
        >
          + Th√™m ph√¢n c√¥ng d·∫°y
        </Button>
      </Box>

      {teaches.length === 0 ? (
        <Alert severity="info">Ch∆∞a c√≥ ph√¢n c√¥ng d·∫°y n√†o</Alert>
      ) : filteredTeaches.length === 0 ? (
        <Alert severity="info">Kh√¥ng t√¨m th·∫•y ph√¢n c√¥ng d·∫°y cho gi√°o vi√™n ƒë∆∞·ª£c ch·ªçn</Alert>
      ) : (
        <TableContainer component={Paper}>
          <Table size="small">
            <TableHead>
              <TableRow sx={{ bgcolor: 'action.hover' }}>
                <TableCell sx={{ fontWeight: 600 }}>Gi√°o vi√™n</TableCell>
                <TableCell sx={{ fontWeight: 600 }}>M√¥n h·ªçc</TableCell>
                <TableCell sx={{ fontWeight: 600 }}>L·ªõp</TableCell>
                <TableCell sx={{ fontWeight: 600 }}>Lo·∫°i</TableCell>
                <TableCell sx={{ fontWeight: 600 }}>Th·ª©</TableCell>
                <TableCell sx={{ fontWeight: 600 }}>Gi·ªù</TableCell>
                <TableCell sx={{ fontWeight: 600 }}>Ghi ch√∫</TableCell>
                <TableCell sx={{ fontWeight: 600, textAlign: 'center' }}>H√†nh ƒë·ªông</TableCell>
              </TableRow>
            </TableHead>
            <TableBody>
              {filteredTeaches.map((teach) => {
                const teacher = teachers.find(t => String(t.id) === String(teach.teacherId));
                const subjectsToUse = allSubjects.length > 0 ? allSubjects : subjects;
                const subject = subjectsToUse.find(s => String(s.id) === String(teach.subjectId));
                
                // Debug - log first record
                if (teach === filteredTeaches[0]) {
                  console.log('üîç TeachesList DEBUG - First Teach:', teach);
                  console.log('üîç TeachesList DEBUG - Teach SubjectId:', teach.subjectId);
                  console.log('üîç TeachesList DEBUG - All Subjects Loaded:', allSubjects.length);
                  console.log('üîç TeachesList DEBUG - First Subject from API:', allSubjects[0]);
                  console.log('üîç TeachesList DEBUG - Found Subject Match:', subject);
                  console.log('üîç TeachesList DEBUG - Subject Hook Length:', subjects.length);
                }

                return (
                  <TableRow key={teach.id} hover>
                    <TableCell>{teacher?.name || 'N/A'}</TableCell>
                    <TableCell>{subject?.name || 'N/A'}</TableCell>
                    <TableCell>{teach.className || 'N/A'}</TableCell>
                    <TableCell>
                      <Chip
                        label={classTypeLabels[teach.classType] || teach.classType}
                        color={classTypeColors[teach.classType] || 'default'}
                        size="small"
                      />
                    </TableCell>
                    <TableCell>{teach.dayOfWeek}</TableCell>
                    <TableCell>{teach.startTime} - {teach.endTime}</TableCell>
                    <TableCell>{teach.notes || '-'}</TableCell>
                    <TableCell sx={{ textAlign: 'center' }}>
                      <Button
                        size="small"
                        onClick={() => teach.id && handleDelete(teach.id)}
                        color="error"
                      >
                        X√≥a
                      </Button>
                    </TableCell>
                  </TableRow>
                );
              })}
            </TableBody>
          </Table>
        </TableContainer>
      )}

      <Dialog open={open} onClose={handleClose} maxWidth="sm" fullWidth>
        <DialogTitle>Th√™m ph√¢n c√¥ng d·∫°y</DialogTitle>
        <DialogContent sx={{ pt: 2, display: 'flex', flexDirection: 'column', gap: 2 }}>
          {submitError && <Alert severity="error">{submitError}</Alert>}
          <FormControl fullWidth>
            <InputLabel>Gi√°o vi√™n</InputLabel>
            <Select
              value={formData.teacherId || ''}
              onChange={(e) => setFormData({ ...formData, teacherId: e.target.value })}
              label="Gi√°o vi√™n"
            >
              {teachers.map((teacher) => (
                <MenuItem key={teacher.id} value={teacher.id}>
                  {teacher.name}
                </MenuItem>
              ))}
            </Select>
          </FormControl>

          <FormControl fullWidth>
            <InputLabel>M√¥n h·ªçc</InputLabel>
            <Select
              value={formData.subjectId || ''}
              onChange={(e) => setFormData({ ...formData, subjectId: e.target.value })}
              label="M√¥n h·ªçc"
            >
              {(allSubjects.length > 0 ? allSubjects : subjects).map((subject) => (
                <MenuItem key={subject.id} value={subject.id}>
                  {subject.name}
                </MenuItem>
              ))}
            </Select>
          </FormControl>

          <FormControl fullWidth>
            <InputLabel>Lo·∫°i l·ªõp</InputLabel>
            <Select
              value={formData.classType || 'fixed'}
              onChange={(e) => setFormData({ ...formData, classType: e.target.value as 'fixed' | 'session' })}
              label="Lo·∫°i l·ªõp"
            >
              <MenuItem value="fixed">L·ªõp c·ªë ƒë·ªãnh</MenuItem>
              <MenuItem value="session">L·ªõp ngo√†i kh√≥a</MenuItem>
            </Select>
          </FormControl>

          <TextField
            fullWidth
            label="T√™n l·ªõp"
            value={formData.className || ''}
            onChange={(e) => setFormData({ ...formData, className: e.target.value })}
            placeholder="VD: L·ªõp 10A"
          />

          <FormControl fullWidth>
            <InputLabel>Th·ª© trong tu·∫ßn</InputLabel>
            <Select
              value={formData.dayOfWeek || 'Th·ª© 2'}
              onChange={(e) => setFormData({ ...formData, dayOfWeek: e.target.value })}
              label="Th·ª© trong tu·∫ßn"
            >
              {daysOfWeek.map((day) => (
                <MenuItem key={day} value={day}>
                  {day}
                </MenuItem>
              ))}
            </Select>
          </FormControl>

          <TextField
            fullWidth
            label="Gi·ªù b·∫Øt ƒë·∫ßu"
            type="time"
            value={formData.startTime || '08:00'}
            onChange={(e) => setFormData({ ...formData, startTime: e.target.value })}
            InputLabelProps={{ shrink: true }}
          />

          <TextField
            fullWidth
            label="Gi·ªù k·∫øt th√∫c"
            type="time"
            value={formData.endTime || '10:00'}
            onChange={(e) => setFormData({ ...formData, endTime: e.target.value })}
            InputLabelProps={{ shrink: true }}
          />

          <TextField
            fullWidth
            label="Ghi ch√∫"
            multiline
            rows={2}
            value={formData.notes || ''}
            onChange={(e) => setFormData({ ...formData, notes: e.target.value })}
          />
        </DialogContent>
        <DialogActions>
          <Button onClick={handleClose}>H·ªßy</Button>
          <Button 
            onClick={handleSubmit} 
            variant="contained"
            disabled={submitting}
          >
            {submitting ? 'ƒêang x·ª≠ l√Ω...' : 'T·∫°o'}
          </Button>
        </DialogActions>
      </Dialog>
    </Box>
  );
}

'use client';

import React, { useEffect, useState } from 'react';
import {
  Box,
  Button,
  Dialog,
  DialogTitle,
  DialogContent,
  DialogActions,
  TextField,
  Select,
  MenuItem,
  FormControl,
  InputLabel,
  Table,
  TableBody,
  TableCell,
  TableContainer,
  TableHead,
  TableRow,
  Paper,
  Chip,
  Alert,
  CircularProgress,
} from '@mui/material';
import { FreeSchedule } from '@/types/schedule';
import { useFreeSchedules } from '@/hooks/useFreeSchedules';

export function FreeSchedulesList() {
  const { freeSchedules, loading, error, fetchAll, create, update, remove } = useFreeSchedules();
  const [open, setOpen] = useState(false);
  const [editingId, setEditingId] = useState<string | null>(null);
  const [formData, setFormData] = useState<Partial<FreeSchedule>>({
    workScheduleId: '',
    teacherId: '',
    dayOfWeek: 'Th·ª© 2',
    startTime: '12:00',
    endTime: '13:00',
    reason: 'break',
    notes: '',
  });

  useEffect(() => {
    fetchAll();
  }, [fetchAll]);

  const handleOpen = (schedule?: FreeSchedule) => {
    if (schedule) {
      setFormData(schedule);
      setEditingId(schedule.id);
    } else {
      setFormData({
        workScheduleId: '',
        teacherId: '',
        dayOfWeek: 'Th·ª© 2',
        startTime: '12:00',
        endTime: '13:00',
        reason: 'break',
        notes: '',
      });
      setEditingId(null);
    }
    setOpen(true);
  };

  const handleClose = () => {
    setOpen(false);
    setEditingId(null);
  };

  const handleSave = async () => {
    try {
      if (editingId) {
        await update(editingId, formData);
      } else {
        await create(formData as Omit<FreeSchedule, 'id' | 'createdAt' | 'updatedAt'>);
      }
      handleClose();
    } catch (err) {
      console.error('L·ªói l∆∞u:', err);
    }
  };

  const handleDelete = async (id: string) => {
    if (confirm('B·∫°n ch·∫Øc ch·∫Øn mu·ªën x√≥a?')) {
      await remove(id);
    }
  };

  const reasonLabels: Record<string, string> = {
    break: '‚òï Gi·∫£i lao',
    lunch: 'üçΩÔ∏è ƒÇn tr∆∞a',
    other: 'üìù Kh√°c',
  };

  const reasonColors: Record<string, 'default' | 'primary' | 'secondary' | 'error' | 'info' | 'success' | 'warning'> = {
    break: 'info',
    lunch: 'success',
    other: 'warning',
  };

  return (
    <Box>
      {error && <Alert severity="error" sx={{ mb: 2 }}>{error}</Alert>}

      <Button variant="contained" onClick={() => handleOpen()} sx={{ mb: 2 }}>
        ‚ûï Th√™m L·ªãch R·∫£nh
      </Button>

      {loading ? (
        <Box sx={{ display: 'flex', justifyContent: 'center', py: 4 }}>
          <CircularProgress />
        </Box>
      ) : (
        <TableContainer component={Paper}>
          <Table size="small">
            <TableHead sx={{ backgroundColor: '#f5f5f5' }}>
              <TableRow>
                <TableCell>Gi√°o vi√™n</TableCell>
                <TableCell>Ng√†y</TableCell>
                <TableCell>Th·ªùi gian</TableCell>
                <TableCell>L√Ω do</TableCell>
                <TableCell>Ghi ch√∫</TableCell>
                <TableCell align="right">H√†nh ƒë·ªông</TableCell>
              </TableRow>
            </TableHead>
            <TableBody>
              {freeSchedules.length > 0 ? (
                freeSchedules.map((fs) => (
                  <TableRow key={fs.id} hover>
                    <TableCell sx={{ fontSize: '0.85rem' }}>{fs.teacherId}</TableCell>
                    <TableCell>{fs.dayOfWeek}</TableCell>
                    <TableCell>
                      {fs.startTime} - {fs.endTime}
                    </TableCell>
                    <TableCell>
                      <Chip
                        label={reasonLabels[fs.reason] || fs.reason}
                        size="small"
                        color={reasonColors[fs.reason]}
                      />
                    </TableCell>
                    <TableCell sx={{ fontSize: '0.85rem' }}>{fs.notes}</TableCell>
                    <TableCell align="right">
                      <Button size="small" onClick={() => handleOpen(fs)}>
                        S·ª≠a
                      </Button>
                      <Button size="small" color="error" onClick={() => handleDelete(fs.id)}>
                        X√≥a
                      </Button>
                    </TableCell>
                  </TableRow>
                ))
              ) : (
                <TableRow>
                  <TableCell colSpan={6} align="center">
                    Kh√¥ng c√≥ d·ªØ li·ªáu
                  </TableCell>
                </TableRow>
              )}
            </TableBody>
          </Table>
        </TableContainer>
      )}

      <Dialog open={open} onClose={handleClose} maxWidth="sm" fullWidth>
        <DialogTitle>{editingId ? '‚úèÔ∏è S·ª≠a L·ªãch R·∫£nh' : '‚ûï Th√™m L·ªãch R·∫£nh'}</DialogTitle>
        <DialogContent sx={{ display: 'flex', flexDirection: 'column', gap: 2, pt: 2 }}>
          <TextField
            label="Work Schedule ID"
            value={formData.workScheduleId || ''}
            onChange={(e) => setFormData({ ...formData, workScheduleId: e.target.value })}
            fullWidth
            size="small"
          />

          <TextField
            label="Teacher ID"
            value={formData.teacherId || ''}
            onChange={(e) => setFormData({ ...formData, teacherId: e.target.value })}
            fullWidth
            size="small"
          />

          <FormControl fullWidth size="small">
            <InputLabel>Ng√†y trong tu·∫ßn</InputLabel>
            <Select
              value={formData.dayOfWeek || 'Th·ª© 2'}
              onChange={(e) => setFormData({ ...formData, dayOfWeek: e.target.value })}
              label="Ng√†y trong tu·∫ßn"
            >
              {['Th·ª© 2', 'Th·ª© 3', 'Th·ª© 4', 'Th·ª© 5', 'Th·ª© 6', 'Th·ª© 7', 'Ch·ªß nh·∫≠t'].map((day) => (
                <MenuItem key={day} value={day}>
                  {day}
                </MenuItem>
              ))}
            </Select>
          </FormControl>

          <TextField
            label="Gi·ªù b·∫Øt ƒë·∫ßu"
            type="time"
            value={formData.startTime || '12:00'}
            onChange={(e) => setFormData({ ...formData, startTime: e.target.value })}
            fullWidth
            size="small"
            InputLabelProps={{ shrink: true }}
          />

          <TextField
            label="Gi·ªù k·∫øt th√∫c"
            type="time"
            value={formData.endTime || '13:00'}
            onChange={(e) => setFormData({ ...formData, endTime: e.target.value })}
            fullWidth
            size="small"
            InputLabelProps={{ shrink: true }}
          />

          <FormControl fullWidth size="small">
            <InputLabel>L√Ω do</InputLabel>
            <Select
              value={formData.reason || 'break'}
              onChange={(e) => setFormData({ ...formData, reason: e.target.value as any })}
              label="L√Ω do"
            >
              <MenuItem value="break">‚òï Gi·∫£i lao</MenuItem>
              <MenuItem value="lunch">üçΩÔ∏è ƒÇn tr∆∞a</MenuItem>
              <MenuItem value="other">üìù Kh√°c</MenuItem>
            </Select>
          </FormControl>

          <TextField
            label="Ghi ch√∫"
            value={formData.notes || ''}
            onChange={(e) => setFormData({ ...formData, notes: e.target.value })}
            fullWidth
            size="small"
            multiline
            rows={2}
          />
        </DialogContent>
        <DialogActions>
          <Button onClick={handleClose}>H·ªßy</Button>
          <Button onClick={handleSave} variant="contained">
            L∆∞u
          </Button>
        </DialogActions>
      </Dialog>
    </Box>
  );
}

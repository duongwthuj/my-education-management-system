'use client';

import React, { useEffect, useState, forwardRef, useImperativeHandle } from 'react';
import {
  Box,
  Button,
  Dialog,
  DialogTitle,
  DialogContent,
  DialogActions,
  TextField,
  Select,
  MenuItem,
  FormControl,
  InputLabel,
  Table,
  TableBody,
  TableCell,
  TableContainer,
  TableHead,
  TableRow,
  Paper,
  Chip,
  Alert,
  CircularProgress,
} from '@mui/material';
import { FreeSchedule } from '@/types/schedule';
import { useFreeSchedules } from '@/hooks/useFreeSchedules';
import { useTeachers } from '@/hooks/use-teachers';

export const FreeSchedulesList = forwardRef(function FreeSchedulesListComponent(_props, ref) {
  const { freeSchedules, loading, error, fetchAll, create, update, remove } = useFreeSchedules();
  const { teachers } = useTeachers();
  const [open, setOpen] = useState(false);
  const [editingId, setEditingId] = useState<string | null>(null);
  const [selectedTeacherId, setSelectedTeacherId] = useState<string>('');
  const [submitting, setSubmitting] = useState(false);
  const [submitError, setSubmitError] = useState<string | null>(null);
  const [formData, setFormData] = useState<Partial<FreeSchedule>>({
    workScheduleId: '',
    teacherId: '',
    dayOfWeek: 'Th·ª© 2',
    startTime: '12:00',
    endTime: '13:00',
    reason: 'break',
    notes: '',
  });

  // Expose refetch method to parent
  useImperativeHandle(ref, () => ({
    refetch: async () => {
      console.log('üîÑ FreeSchedulesList: Refetching from parent trigger...');
      await fetchAll();
    },
  }), [fetchAll]);

  // Filter schedules by selected teacher
  const filteredFreeSchedules = selectedTeacherId 
    ? freeSchedules.filter(fs => String(fs.teacherId) === selectedTeacherId)
    : freeSchedules;

  useEffect(() => {
    fetchAll();
  }, [fetchAll]);

  const handleOpen = (schedule?: FreeSchedule) => {
    if (schedule) {
      setFormData(schedule);
      setEditingId(schedule.id);
    } else {
      setFormData({
        workScheduleId: '',
        teacherId: '',
        dayOfWeek: 'Th·ª© 2',
        startTime: '12:00',
        endTime: '13:00',
        reason: 'break',
        notes: '',
      });
      setEditingId(null);
    }
    setOpen(true);
  };

  const handleClose = () => {
    setOpen(false);
    setEditingId(null);
    setSubmitError(null);
  };

  const handleSave = async () => {
    try {
      setSubmitting(true);
      setSubmitError(null);

      // Validation
      if (!formData.teacherId || !formData.dayOfWeek || !formData.startTime || !formData.endTime || !formData.reason) {
        setSubmitError('Vui l√≤ng ƒëi·ªÅn t·∫•t c·∫£ c√°c tr∆∞·ªùng b·∫Øt bu·ªôc');
        return;
      }

      if (editingId) {
        await update(editingId, formData);
      } else {
        await create(formData as Omit<FreeSchedule, 'id' | 'createdAt' | 'updatedAt'>);
      }
      
      await fetchAll();
      handleClose();
    } catch (err) {
      setSubmitError(err instanceof Error ? err.message : 'C√≥ l·ªói x·∫£y ra');
    } finally {
      setSubmitting(false);
    }
  };

  const handleDelete = async (id: string) => {
    if (!confirm('B·∫°n c√≥ ch·∫Øc ch·∫Øn mu·ªën x√≥a l·ªãch r·∫£nh n√†y?')) {
      return;
    }
    
    try {
      await remove(id);
      await fetchAll();
    } catch (err) {
      alert('L·ªói x√≥a: ' + (err instanceof Error ? err.message : 'C√≥ l·ªói x·∫£y ra'));
    }
  };

  const reasonLabels: Record<string, string> = {
    break: '‚òï Gi·∫£i lao',
    lunch: 'üçΩÔ∏è ƒÇn tr∆∞a',
    other: 'üìù Kh√°c',
  };

  const reasonColors: Record<string, 'default' | 'primary' | 'secondary' | 'error' | 'info' | 'success' | 'warning'> = {
    break: 'info',
    lunch: 'success',
    other: 'warning',
  };

  return (
    <Box>
      {error && <Alert severity="error" sx={{ mb: 2 }}>{error}</Alert>}

      <Box sx={{ mb: 3, display: 'flex', gap: 2, alignItems: 'flex-end' }}>
        <FormControl sx={{ minWidth: 250 }}>
          <InputLabel>L·ªçc theo gi√°o vi√™n</InputLabel>
          <Select
            value={selectedTeacherId}
            onChange={(e) => setSelectedTeacherId(e.target.value)}
            label="L·ªçc theo gi√°o vi√™n"
          >
            <MenuItem value="">T·∫•t c·∫£ gi√°o vi√™n</MenuItem>
            {teachers.map((teacher) => (
              <MenuItem key={teacher.id} value={String(teacher.id)}>
                {teacher.name}
              </MenuItem>
            ))}
          </Select>
        </FormControl>
        <Button variant="contained" onClick={() => handleOpen()}>
          ‚ûï Th√™m L·ªãch R·∫£nh
        </Button>
      </Box>

      {loading ? (
        <Box sx={{ display: 'flex', justifyContent: 'center', py: 4 }}>
          <CircularProgress />
        </Box>
      ) : (
        <TableContainer component={Paper}>
          <Table size="small">
            <TableHead sx={{ backgroundColor: '#f5f5f5' }}>
              <TableRow>
                <TableCell>Gi√°o vi√™n</TableCell>
                <TableCell>Ng√†y</TableCell>
                <TableCell>Th·ªùi gian</TableCell>
                <TableCell>L√Ω do</TableCell>
                <TableCell>Ghi ch√∫</TableCell>
                <TableCell align="right">H√†nh ƒë·ªông</TableCell>
              </TableRow>
            </TableHead>
            <TableBody>
              {filteredFreeSchedules.length > 0 ? (
                filteredFreeSchedules.map((fs) => {
                  return (
                  <TableRow key={fs.id} hover>
                    <TableCell sx={{ fontSize: '0.85rem' }}>{(fs as any).teacherName || 'N/A'}</TableCell>
                    <TableCell>{fs.dayOfWeek}</TableCell>
                    <TableCell>
                      {fs.startTime} - {fs.endTime}
                    </TableCell>
                    <TableCell>
                      <Chip
                        label={reasonLabels[fs.reason] || fs.reason}
                        size="small"
                        color={reasonColors[fs.reason]}
                      />
                    </TableCell>
                    <TableCell sx={{ fontSize: '0.85rem' }}>{fs.notes}</TableCell>
                    <TableCell align="right">
                      <Button size="small" onClick={() => handleOpen(fs)}>
                        S·ª≠a
                      </Button>
                      <Button size="small" color="error" onClick={() => handleDelete(fs.id)}>
                        X√≥a
                      </Button>
                    </TableCell>
                  </TableRow>
                );
                })
              ) : (
                <TableRow>
                  <TableCell colSpan={6} align="center">
                    Kh√¥ng c√≥ d·ªØ li·ªáu
                  </TableCell>
                </TableRow>
              )}
            </TableBody>
          </Table>
        </TableContainer>
      )}

      <Dialog open={open} onClose={handleClose} maxWidth="sm" fullWidth PaperProps={{ elevation: 8 }}>
        <DialogTitle>{editingId ? '‚úèÔ∏è S·ª≠a L·ªãch R·∫£nh' : '‚ûï Th√™m L·ªãch R·∫£nh'}</DialogTitle>
        <DialogContent sx={{ display: 'flex', flexDirection: 'column', gap: 2, pt: 2 }}>
          {submitError && <Alert severity="error">{submitError}</Alert>}
          
          <FormControl fullWidth size="small">
            <InputLabel>Gi√°o vi√™n</InputLabel>
            <Select
              value={formData.teacherId || ''}
              onChange={(e) => setFormData({ ...formData, teacherId: e.target.value })}
              label="Gi√°o vi√™n"
            >
              {teachers.map((teacher) => (
                <MenuItem key={teacher.id} value={teacher.id}>
                  {teacher.name}
                </MenuItem>
              ))}
            </Select>
          </FormControl>

          <FormControl fullWidth size="small">
            <InputLabel>Ng√†y trong tu·∫ßn</InputLabel>
            <Select
              value={formData.dayOfWeek || 'Th·ª© 2'}
              onChange={(e) => setFormData({ ...formData, dayOfWeek: e.target.value })}
              label="Ng√†y trong tu·∫ßn"
            >
              {['Th·ª© 2', 'Th·ª© 3', 'Th·ª© 4', 'Th·ª© 5', 'Th·ª© 6', 'Th·ª© 7', 'Ch·ªß nh·∫≠t'].map((day) => (
                <MenuItem key={day} value={day}>
                  {day}
                </MenuItem>
              ))}
            </Select>
          </FormControl>

          <TextField
            label="Gi·ªù b·∫Øt ƒë·∫ßu"
            type="time"
            value={formData.startTime || '12:00'}
            onChange={(e) => setFormData({ ...formData, startTime: e.target.value })}
            fullWidth
            size="small"
            InputLabelProps={{ shrink: true }}
          />

          <TextField
            label="Gi·ªù k·∫øt th√∫c"
            type="time"
            value={formData.endTime || '13:00'}
            onChange={(e) => setFormData({ ...formData, endTime: e.target.value })}
            fullWidth
            size="small"
            InputLabelProps={{ shrink: true }}
          />

          <FormControl fullWidth size="small">
            <InputLabel>L√Ω do</InputLabel>
            <Select
              value={formData.reason || 'break'}
              onChange={(e) => setFormData({ ...formData, reason: e.target.value as any })}
              label="L√Ω do"
            >
              <MenuItem value="break">‚òï Gi·∫£i lao</MenuItem>
              <MenuItem value="lunch">üçΩÔ∏è ƒÇn tr∆∞a</MenuItem>
              <MenuItem value="other">üìù Kh√°c</MenuItem>
            </Select>
          </FormControl>

          <TextField
            label="Ghi ch√∫"
            value={formData.notes || ''}
            onChange={(e) => setFormData({ ...formData, notes: e.target.value })}
            fullWidth
            size="small"
            multiline
            rows={2}
          />
        </DialogContent>
        <DialogActions>
          <Button onClick={handleClose}>H·ªßy</Button>
          <Button 
            onClick={handleSave} 
            variant="contained"
            disabled={submitting}
          >
            {submitting ? 'ƒêang x·ª≠ l√Ω...' : 'L∆∞u'}
          </Button>
        </DialogActions>
      </Dialog>
    </Box>
  );
});
